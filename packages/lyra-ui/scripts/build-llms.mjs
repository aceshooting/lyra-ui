#!/usr/bin/env node
// Builds every generated agent-facing reference artifact from the authored sources in `llms/`.
//
// Authored (edit these):
//   llms/00-preamble.md       intro paragraphs of llms-full.txt
//   llms/shared.md            foundation + cross-cutting notes (apply to the whole library)
//   llms/<family>.md          one file per src/components/<family>/ directory
//
// Generated (never edit; `pnpm run llms` rewrites them, CI diffs them):
//   llms-full.txt             concatenation, the single-file reference published since v1
//   llms/index.md             tag -> family/import/one-line-purpose routing table
//   llms/components/<tag>.md  one self-contained file per custom element (surgical agent reads)
//   llms/tokens.md            every --lr-* token and the --lr-theme-* input it reads
//   llms/peers.md             component -> optional peer dependency map
//   llms/migration.md         wa-*/sl-* -> lr-* rename table
//
// A component section is assigned to a family by the src/components/<family>/ directory its tag is
// declared in, so the docs can never drift from the source tree's own grouping.
import { readFileSync, writeFileSync, mkdirSync, rmSync, readdirSync, existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildMirrorMap } from './migrate-wa.mjs';

const packageDir = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const llmsDir = path.join(packageDir, 'llms');
const read = (...p) => readFileSync(path.join(packageDir, ...p), 'utf8');

/** Human-readable family titles, in the order they appear in llms-full.txt. */
export const FAMILIES = [
  ['forms', 'Form controls & inputs'],
  ['data', 'Data display, dashboards & flow canvas'],
  ['layout', 'Layout, navigation & structure'],
  ['overlays', 'Overlays, status & feedback'],
  ['utility', 'Utilities, formatting & observers'],
  ['media', 'Media, files & maps'],
  ['charts', 'Charts'],
  ['viewers', 'Document & file viewers'],
  ['conversation', 'Conversation & chat UI'],
  ['agent-tools', 'Agent tooling, tool calls & evaluation'],
  ['retrieval', 'Retrieval, knowledge graph & grounding'],
];

const GENERATED = (source) =>
  `<!-- GENERATED by scripts/build-llms.mjs from ${source} — do not edit this file. -->`;

// ---------------------------------------------------------------------------------------------
// Manifest facts: tag -> family, declaring module, and the registration entry point to import.
// ---------------------------------------------------------------------------------------------

/**
 * One-line purpose from a class's JSDoc: drop the leading "`<lr-x>` — " restatement, unwrap the
 * source's hard line breaks, and keep the first sentence (or the first clause, when the JSDoc opens
 * with a long enumeration).
 */
function summarize(description) {
  const flat = description.split('\n\n')[0].replace(/\s+/g, ' ').trim();
  const body = flat.replace(/^`?<?lr-[a-z0-9-]+>?`?\s*[—-]\s*/i, '');
  const sentence = body.match(/^(.+?\.)(?:\s|$)/)?.[1] ?? body;
  return sentence.length > 140 ? `${sentence.slice(0, 137).trimEnd()}...` : sentence;
}

export function readTagFacts(manifest) {
  const tags = new Map();
  for (const mod of manifest.modules ?? []) {
    const m = mod.path.match(/^src\/components\/([^/]+)\/([^/]+)\//);
    if (!m) continue;
    for (const decl of mod.declarations ?? []) {
      if (!decl.customElement || !decl.tagName) continue;
      // `foo.class.ts` declares the class; `foo.ts` next to it is the side-effect entry that
      // registers the tag — that is what a consumer imports.
      const entry = mod.path.replace(/\.class\.ts$/, '.ts').replace(/^src\//, '').replace(/\.ts$/, '.js');
      tags.set(decl.tagName, {
        tag: decl.tagName,
        family: m[1],
        className: decl.name,
        importPath: `@aceshooting/lyra-ui/${entry}`,
        modulePath: mod.path,
        purpose: summarize(decl.description ?? decl.summary ?? ''),
        cssParts: decl.cssParts ?? [],
        cssProperties: decl.cssProperties ?? [],
      });
    }
  }
  return tags;
}

// ---------------------------------------------------------------------------------------------
// Section parsing: a `## ` heading opens a section; `###` subheadings stay inside it.
// ---------------------------------------------------------------------------------------------

export function splitSections(text) {
  const lines = text.split('\n');
  const sections = [];
  let fenced = false;
  let current = null;
  for (const line of lines) {
    if (/^```/.test(line)) fenced = !fenced;
    if (!fenced && /^## /.test(line)) {
      current = { title: line.slice(3).trim(), lines: [line], tags: [] };
      sections.push(current);
      continue;
    }
    if (!current) continue;
    current.lines.push(line);
    if (!fenced && /^#{3,} /.test(line)) current.tags.push(...(line.match(/lr-[a-z0-9-]+/g) ?? []));
  }
  for (const s of sections) {
    s.tags = [...new Set([...(s.title.match(/lr-[a-z0-9-]+/g) ?? []), ...s.tags])];
    s.text = s.lines.join('\n').replace(/\n+$/, '');
  }
  return sections;
}

/** First sentence of a section's prose — used as the one-line purpose in the index. */
function firstSentence(section) {
  const body = section.text.split('\n').slice(1).join('\n');
  const prose = body
    .split(/\n\s*\n/)
    .map((p) => p.trim())
    .find((p) => p && !p.startsWith('#') && !p.startsWith('```') && !p.startsWith('|'));
  if (!prose) return '';
  const flat = prose.replace(/\n/g, ' ').replace(/\s+/g, ' ');
  const cut = flat.match(/^(.+?\.)(?:\s|$)/);
  return (cut ? cut[1] : flat).trim();
}

